export const CATEGORY_ICONS = Object.keys(EMOJI_CATEGORIES).map(k => ({
    key: k,
    icon: k.split(' ')[0],
    label: k.split(' ').slice(1).join(' '),
}));

export default defineComponent({
    name: 'EmojiPicker',

    emits: ['select', 'close'],

    setup(props, { emit }) {
        const activeCategory = ref(Object.keys(EMOJI_CATEGORIES)[0]);
        const searchQuery = ref('');

        const filteredEmojis = computed(() => {
            const cat = EMOJI_CATEGORIES[activeCategory.value] || [];
            if (!searchQuery.value) return cat;
            return Object.values(EMOJI_CATEGORIES)
                .flat()
                .filter(e => e.includes(searchQuery.value));
        });

        const selectEmoji = (emoji) => {
            emit('select', emoji);
        };

        return {
            EMOJI_CATEGORIES,
            CATEGORY_ICONS,
            activeCategory,
            searchQuery,
            filteredEmojis,
            selectEmoji,
        };
    },
});
